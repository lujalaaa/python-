"""multiplication table using for loop and also using while loop"""
print("Using For loop:")
for x in range(1,10):
  print('5*',x,"=",5*x) 
  
  
  
  
print("using while loop")
i=1
while i<10:
    print("6*",i,"=",6*i)
    i+=1
    